namespace Lecture1.StateMachine.GoodCase
{
    /// <summary>
    /// Контракт (интерфейс, interface), которому подчиняется каждое состояние игрока.
    /// 
    /// Паттерн State строится на трёх ключевых методах:
    ///   Enter() — вызывается ОДИН РАЗ при входе в состояние (инициализация),
    ///   Exit()  — вызывается ОДИН РАЗ при выходе (очистка за собой),
    ///   Tick()  — вызывается КАЖДЫЙ КАДР, пока состояние активно (основная логика).
    /// 
    /// Машина состояний работает только с этим интерфейсом и ничего
    /// не знает о конкретных состояниях — это позволяет добавлять
    /// новые состояния, не изменяя код самой машины.
    /// </summary>
    public interface IState
    {
        void Enter();
        void Exit();
        void Tick();
    }
}